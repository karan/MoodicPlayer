	/**
	 * @return true if user has already authenticated.
	 */
	private boolean isAuth() throws IOException {
		File file = new File("user.properties");
		if (file.exists()) {
			BufferedReader br = new BufferedReader(new FileReader(file));
			String sessionKey = br.readLine();
			return sessionKey.length() == 36; // TODO: may need to change
		}
		return false;
	}

